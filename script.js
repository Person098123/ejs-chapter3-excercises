function min(a, b){
    if(a < b) return a;
       else return b;   
}

console.log(min(0, 10));
// → 0
console.log(min(0, -10));
// → -10

//Recursion excercise

function isEven(n){
    if(n % 2 == 0) return true;
    else return false;
}

console.log(isEven(50));
// → true
console.log(isEven(75));
// → false
console.log(isEven(-1));
// → false

